/// A typed attribute definition generated by `#[derive(Attribute)]`.
///
/// Each attribute newtype (e.g. `struct Name(String)`) implements this
/// trait, which provides:
/// - The scalar Rust type of the attribute's values (`Type`).
/// - Access to the inner value via [`value()`](Attribute::value).
///
/// Attributes are the atomic building blocks of concepts: a concept is
/// a set of attributes that together describe an entity type.
///
/// For schema metadata, see [`Descriptor<AttributeDescriptor>`](crate::Descriptor).
/// For the fluent `::of()` expression builder, see [`StaticAttributeExpressionBuilder`].
pub trait Attribute: Predicate + Sized {
    /// The Rust scalar type of this attribute's values.
    type Type: Scalar;

    /// Returns a reference to the inner value.
    fn value(&self) -> &Self::Type;
}

